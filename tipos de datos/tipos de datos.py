# Números:

# Entero (numeros enteros)
edad = 25  # Tipo `int`

# Decimal (numero con coma)
altura = 1.75  # Tipo `float`

# Palabras (textos)
nombre = "Ana"  # Tipo `str`

# Verdadero o falso (para tomar decisiones)
es_mayor_de_edad = True  # Tipo `bool`

# Listas (para ordenar cosas)
frutas = ["manzana", "banana", "naranja"]  # Tipo `list`

# Diccionarios (para guardar información de forma organizada)
persona = {"nombre": "Ana", "edad": 25}  # Tipo `dict`

# Conjuntos (para evitar que se repitan cosas)
alumnos = {"Ana", "Juan", "María"}  # Tipo `set`

# Ejemplo de uso:

# Imprimir el nombre y la edad de una persona
print(f"Nombre: {persona['nombre']}")
print(f"Edad: {persona['edad']}")
