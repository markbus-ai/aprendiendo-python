# Declaración de una variable llamada 'nombre' con el valor 'Juan'
nombre = 'Juan'

# Imprimir el valor de la variable 'nombre'
print(nombre)  # Output: Juan

# Cambio del valor de la variable 'nombre'
nombre = 'María'

# Imprimir el nuevo valor de la variable 'nombre'
print(nombre)  # Output: María

# Declaración de una variable numérica llamada 'edad' con el valor 25
edad = 25

# Imprimir el valor de la variable 'edad'
print(edad)  # Output: 25

# Operaciones con variables numéricas
edad = edad + 5  # Incrementar la edad en 5 años

# Imprimir el nuevo valor de la variable 'edad'
print(edad)  # Output: 30

# Declaración de una variable booleana llamada 'es_mayor_de_edad'
es_mayor_de_edad = True

# Imprimir el valor de la variable 'es_mayor_de_edad'
print(es_mayor_de_edad)  # Output: True

# Declaración de una lista llamada 'numeros'
numeros = [1, 2, 3, 4, 5]

# Imprimir la lista de números
print(numeros)  # Output: [1, 2, 3, 4, 5]

# Acceso a elementos de la lista por índice
print(numeros[0])  # Output: 1
print(numeros[2])  # Output: 3

# Modificación de un elemento de la lista
numeros[0] = 10

# Imprimir la lista actualizada
print(numeros)  # Output: [10, 2, 3, 4, 5]

# Declaración de un diccionario llamado 'persona' con diferentes atributos
persona = {'nombre': 'Ana', 'edad': 30, 'ciudad': 'Madrid'}

# Imprimir el diccionario 'persona'
print(persona)  # Output: {'nombre': 'Ana', 'edad': 30, 'ciudad': 'Madrid'}

# Acceso a valores del diccionario por clave
print(persona['nombre'])  # Output: Ana
print(persona['edad'])    # Output: 30

# Modificación de un valor en el diccionario
persona['ciudad'] = 'Barcelona'

# Imprimir el diccionario actualizado
print(persona)  # Output: {'nombre': 'Ana', 'edad': 30, 'ciudad': 'Barcelona'}
